const SIGNATURE = '// generated by genversion'

exports.SIGNATURE = SIGNATURE

/**
 * returns version content
 * @param {string} version
 * @param {any} opts optional object with optional properties
 * @param {boolean} opts.useSemicolon true to use semicolons in generated code
 * @param {boolean} opts.useEs6Syntax true use ES6 syntax (export/import) in generated code
 */
exports.createContent = function (version, opts) {
  var content = SIGNATURE + (opts.useEs6Syntax ? '\nexport const version = \'' : '\nmodule.exports = \'') + version + '\''

  // Some code styles use semicolons, others do not.
  if (opts.useSemicolon) {
    content += ';\n'
  } else {
    content += '\n'
  }
  return content
}
